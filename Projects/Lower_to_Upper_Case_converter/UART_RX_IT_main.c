/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* MCU Configuration--------------------------------------------------------*/
  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();
  
  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  GPIO_Init();
  UART_Init();
  
  /* Receiving data over UART*/
  if( HAL_UART_Receive_IT(&huart, &rcvd_byte, (uint8_t)1) != HAL_OK)
   {
  	  Error_Handler();
   }

  /* Infinite loop */
  while (1)
  {
	  /* Do nothing but Do not remove this infinite function as
	   * it is the heart of the MCU to keep running the certain
	   * application forever.
	   * */
  } //eo indefinite while loop

} //eo main::

