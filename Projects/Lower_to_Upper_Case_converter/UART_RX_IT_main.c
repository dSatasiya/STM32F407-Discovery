/**
 * @brief UART Initialization Function
 * @param None
 * @retval None
 * */
void UART_Init(void)
{
	/*Configuring the UART as Tx_Rx for 8N1 with 115200 baud rate*/
	huart.Instance = UART4;

	huart.Init.Mode = UART_MODE_TX_RX;
	huart.Init.BaudRate = 115200;
	huart.Init.Parity = UART_PARITY_NONE;
	huart.Init.StopBits = UART_STOPBITS_1;
	huart.Init.WordLength = UART_WORDLENGTH_8B;
	huart.Init.HwFlowCtl = UART_HWCONTROL_NONE;
	huart.Init.OverSampling = UART_OVERSAMPLING_16;

	if( HAL_UART_Init(&huart) != HAL_OK)
	{
		Error_Handler();
	}

} //eo UART_Init::

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if( huart->Instance == UART4)
	{
		/* checking if user entered line feed (LF) */
		if(rcvd_byte == '\r')
		{
			receive_buff[count++] = '\r';
      
      /* Resetting the msg buffer */
			memset(msg, 0, sizeof(msg));
			sprintf(msg, "%s", receive_buff);
      
  		/* Sending data over UART */
			if( HAL_UART_Transmit(huart, (uint8_t*)msg, (uint16_t)strlen(msg), HAL_MAX_DELAY) != HAL_OK)
			{
				Error_Handler();
			}

      /* Resetting the count and receive buffer to keep running the application correctly */
			count = 0;
			memset(receive_buff, 0, sizeof(receive_buff));
		}
		else
		{
			receive_buff[count++] = upper_case(rcvd_byte);
		}

		/* Re-arm to reveive the next character */
		if( HAL_UART_Receive_IT(huart, &rcvd_byte, (uint8_t)1) != HAL_OK)
		{
			Error_Handler();
		}
	}
} //eo HAL_UART_RxCpltCallback::

char upper_case(char data)
{
	if(data >= 'a' && data <= 'z')
	{
		/* means received byte is in lower case */
		data = data - 32;
	}
	return data;
}//eo upper_case::

